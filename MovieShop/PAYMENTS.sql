CREATE TABLE "APP".PAYMENTS (
	ORDERID INTEGER NOT NULL,
	USERID INTEGER NOT NULL,
	AMOUNT DOUBLE NOT NULL,
	DATE DATE DEFAULT CURRENT_DATE NOT NULL,
	TYPE VARCHAR(20) DEFAULT 'PAYPAL' NOT NULL,
	STATUS VARCHAR(20) DEFAULT 'SUBMITTED' NOT NULL,
	PAYMENTID INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1),
	CARDNUM INTEGER,
	CARDNAME VARCHAR(50),
	CARDEXPIRY VARCHAR(6),
	PAYPALTRANSID VARCHAR(20),
	CONSTRAINT primaryKey PRIMARY KEY (PAYMENTID),
	CONSTRAINT chkType CHECK (TYPE IN ('WALLET','CREDIT_CARD','VISA','MASTER_CARD','BANK_TRANSFER','PAYPAL')),
	CONSTRAINT chkStatus CHECK (STATUS IN ('SUBMITTED','SAVED','PROCESSING','ACCEPTED','DECLINED'))
);
